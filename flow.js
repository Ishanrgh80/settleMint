const buy = async(user) => {
    let txObj = await tickets.methods.buyTicket();
    const gas = await txObj.estimateGas({user});
    const gasPrice = await web3.eth.getGasPrice();
    const data = txObj.encodeABI();

    const txn = {
        to: tickets.options.address,
        data,
        gas,
        gasPrice,
    }

    const sign = await web3.eth.accounts.signTransaction(txn,USER_1_KEY);
    const receipt = await web3.eth.sendSignedTransaction(sign.rawTransaction);
    console.log({receipt}); 
}




