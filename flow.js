const rpc = URL;
const web3 = new Web3(rpc);

const tickets = new web3.eth.Contract(ticketABI,ticket_address);

const mintFunction = async(from) => {
    let txObj = await tickets.methods.mintTicket();
    const gas = await txObj.estimateGas({from});
    const gasPrice = await web3.eth.getGasPrice();
    const data = txObj.encodeABI();

    const txn = {
        to: tickets.options.address,
        data,
        gas,
        gasPrice,
    }

    const sign = await web3.eth.accounts.signTransaction(txn,PRIVATE_KEY);
    const receipt = await web3.eth.sendSignedTransaction(sign.rawTransaction);
    console.log({receipt});
}

